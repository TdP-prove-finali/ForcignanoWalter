package model;

public class Peso {
	
	private double distanza;
	private double tempo;
	
	
	public Peso(double distanza, double tempo) {
		super();
		this.distanza = distanza;
		this.tempo = tempo;
	}


	public double getDistanza() {
		return distanza;
	}


	public double getTempo() {
		return tempo;
	}
	
	

}
